<h2 style="text-align: center;">关于我们</h2></br>
<div style="font-size: 16px;">
<span style="margin-left: 32px;"></span>哈啰清华团队成立于2014年3月，我们致力于为清华的同学打造方便快捷、质量可靠、价格实惠的校园综合服务平台——哈啰清华网站（<a href="/">www.hellothu.com</a>）。我们将积极服务清华大学的广大师生，努力吸引优质商家在我们的平台上提供服务，尽力支持学生、社团的公益项目，以我们最大的努力让同学们的校园生活更加舒适、丰富！
<div style="margin-top: 9px;"></div>
<span style="margin-left: 32px;"></span>经过团队成员的努力，哈啰清华网站（www.hellothu.com）目前已经初具规模，完成了网站架构的搭建，并与水果汇道合作，向同学们提供优质价廉的水果团购。并会在不久的将来推出水果当日送达、图书团购等服务来进一步满足同学们的日常需求。欢迎同学们积极为我们的工作提出宝贵意见，我们会及时改正、完善；也欢迎有兴趣的同学加入我们，共同开创这份事业。
<br/>
<div style="margin-top: 9px;"></div>
<span style="margin-left: 32px;"></span>哈啰清华团队是完全独立、自主运行的创业团队，一直积极争取与校内各学生组织合作、交流。目前，在网站建设和宣传推广上已得到校学生会和各院系学生会的支持，但哈啰清华网站上所提供的服务和商品不涉及到任何院系、学生组织、社团和非团队的学生个人的行为和利益。我们愿意用我们最优质的产品和服务来让我们的校园越来越好！
